package ir.koshangas.pasargad.basket.domain;

public class basketDomain {
    private String dateShow;
    private String mobileShow;
    private String nameShow;
    private String factorShow;
    private String totalShow;
    private String paymentShow;
    private String postShow;

    public String getDateShow() {
        return dateShow;
    }

    public void setDateShow(String dateShow) {
        this.dateShow = dateShow;
    }

    public String getMobileShow() {
        return mobileShow;
    }

    public void setMobileShow(String mobileShow) {
        this.mobileShow = mobileShow;
    }

    public String getNameShow() {
        return nameShow;
    }

    public void setNameShow(String nameShow) {
        this.nameShow = nameShow;
    }

    public String getFactorShow() {
        return factorShow;
    }

    public void setFactorShow(String factorShow) {
        this.factorShow = factorShow;
    }

    public String getTotalShow() {
        return totalShow;
    }

    public void setTotalShow(String totalShow) {
        this.totalShow = totalShow;
    }

    public String getPaymentShow() {
        return paymentShow;
    }

    public void setPaymentShow(String paymentShow) {
        this.paymentShow = paymentShow;
    }

    public String getPostShow() {
        return postShow;
    }

    public void setPostShow(String postShow) {
        this.postShow = postShow;
    }
}
